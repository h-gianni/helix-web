  // Helper function to convert MemberData to TeamMember
  export const convertToTeamMember = (member: MemberData): TeamMember => ({
    id: member.id,
    name: member.name,
    email: member.email,
    title: member.title,
  });
  
  // Create a map of team members by team ID
  export const getTeamMembersMap = () => {
    const result: Record<string, TeamMember[]> = {};
    
    // Populate with members
    MEMBERS_DATA.forEach(member => {
      if (!result[member.teamId]) {
        result[member.teamId] = [];
      }
      result[member.teamId].push(convertToTeamMember(member));
    });
    
    return result;
  };
  
  // Get members for a specific team with optional count limit
  export const getTeamMembers = (teamId: string, count?: number): TeamMember[] => {
    const members = MEMBERS_DATA
      .filter(member => member.teamId === teamId)
      .map(convertToTeamMember);
    
    return count ? members.slice(0, count) : members;
  };
  
